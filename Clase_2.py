#  Con las variables también poodemos poner números decimales:
numede = "3.0"
print(numede)
# Hay otras formas de sumar:
one = 1
two = 2
Three = one + two
print(Three)
# También funciona con palabras (SIN OLVIDAR LAS COMILLAS):
hello = "Hello"
World = "World"
helloWorld = hello + " " + World
print(helloWorld)
# Podemos poner cosa simultaneas en la misma linea:
a, b = 3, 4
print(a, b)
# Poner numeros y texto en la misma línea no trabajará.
# Podemos poner condicionales:
x = 1
# SIEMPRE EN "if" RECORDAR LOS ":"
if x == 1:
    print("x is 1")
# Despues de un "if" se deja ese espacio o sangría.
print("Goodbay, World!")
